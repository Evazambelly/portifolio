/*
Operadores lógicos 
&& -> AND -> E = todas as expreções tem que ser verdadeiras para retornar verdadeiro
|| -> OR -> OU
! -> Not -> NãO
*/ 

console.log(true && true && true && true); // se apenas um for false vai retornar false quando executado

const expressaoAnd = true && true && true;
console.log(expressaoAnd);

const expressaoOr = true || false; // se qualque uma das expressões fpr true vai retornar pra mim true
console.log(expressaoOr);

console.log(!false); // retorna true pq ! significa não
// se add mais de um '!' ex: !!false vai retornar falso, porque a segunda ! negou a primeira














