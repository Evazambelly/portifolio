let num1 = 9.3926422;
let num2 = Math.floor(num1); // funçao para arredodar o number para baixo
console.log(num2);
let num3 = Math.ceil(num1); // funçao que arredonda para cima
console.log (num3);
let num4 = Math.round(num1); // arredonda para o numero mais proximo, para baixo ou para cima
console.log (num4);
